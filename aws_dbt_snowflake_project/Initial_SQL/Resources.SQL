CREATE FILE FORMAT IF NOT EXISTS csv_format
TYPE='CSV'
FIELD_DELIMITER = ','
SKIP_HEADER = 1
ERROR_ON_COLUMN_COUNT_MISMATCH = FALSE;

SHOW FILE FORMATS;

CREATE OR REPLACE STAGE snowstage
FILE_FORMAT = csv_format
URL = 's3://snowbucketakhilesh/source/';

SHOW STAGES;

COPY INTO BOOKINGS
FROM @snowstage
FILES = ('bookings.csv')
CREDENTIALS = (aws_key_id = 'use own key',aws_secret_key='use own secret key');

COPY INTO LISTINGS
FROM @snowstage
FILES = ('listings.csv')
CREDENTIALS = (aws_key_id = 'use own key',aws_secret_key='use own secret key');

COPY INTO HOSTS
FROM @snowstage
FILES = ('hosts.csv')
CREDENTIALS = (aws_key_id = 'use own key',aws_secret_key='use own secret key');

SELECT * FROM BOOKINGS;

SELECT * FROM LISTINGS;

SELECT * FROM HOSTS;